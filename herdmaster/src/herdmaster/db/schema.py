class PgCursor:
    """Small DB-API compatibility wrapper around a psycopg cursor."""

    def __init__(self, cursor: psycopg.Cursor[Any]) -> None:
        self._cursor = cursor

    @property
    def rowcount(self) -> int:
        return self._cursor.rowcount

    @property
    def lastrowid(self) -> int | None:
        row = self.fetchone()
        if row is None:
            return None
        if isinstance(row, dict):
            return int(row.get("id")) if row.get("id") is not None else None
        return int(row[0])

    def fetchone(self) -> Any:
        return self._cursor.fetchone()

    def fetchall(self) -> list[Any]:
        return list(self._cursor.fetchall())

    def __iter__(self):
        return iter(self._cursor)


class PgConnection:
    """Connection facade preserving the subset of the DB API used by HerdMaster.

    DECISION: Add safe_execute wrapper with automatic rollback on error
    SOURCE:   https://www.psycopg.org/psycopg3/docs/basic/transactions.html
    REF:      "Transaction contexts" — rollback on exception exit
    RATIONALE: Prevent connection from staying in aborted transaction state
              which blocks all subsequent SQL until explicit rollback.
    VERSION:  psycopg 3.3.x
    """

    def __init__(self, conn: psycopg.Connection[Any], schema_name: str) -> None:
        self._conn = conn
        self.schema_name = schema_name
        self._set_search_path()

    def _set_search_path(self) -> None:
        with self._conn.cursor() as cur:
            cur.execute(
                sql.SQL("SET search_path TO {}, public").format(sql.Identifier(self.schema_name))
            )
        self._conn.commit()

    def execute(self, query: str, params: Any = None) -> PgCursor:
        try:
            cur = self._conn.cursor()
            cur.execute(_translate_placeholders(query), params)
            return PgCursor(cur)
        except Exception:
            # Auto-rollback on failure to prevent the connection from staying
            # in psycopg's InFailedTransaction state, which would cause all
            # subsequent SQL to fail with "current transaction is aborted".
            # Only attempt when pgconn reports INERROR.  When inside a
            # psycopg3 ``with conn.transaction():`` context, explicit
            # rollback raises ProgrammingError — that is safe to ignore
            # because the context manager handles rollback on exit.
            try:
                from psycopg.pq import TransactionStatus
                if self._conn.pgconn.transaction_status == TransactionStatus.INERROR:
                    self.rollback()
            except Exception as rb_exc:
                # Suppress "Explicit rollback() forbidden within a
                # Transaction context" — psycopg3 handles this for us.
                if "Transaction context" not in str(rb_exc):
                    logger.warning("rollback after execute failure also failed", exc_info=True)
            raise

    def safe_execute(self, query: str, params: Any = None) -> PgCursor:
        """Execute SQL with automatic rollback on error.

        If the query fails, conn.rollback() is called before re-raising
        so the connection does not remain in an aborted transaction state.
        """
        try:
            return self.execute(query, params)
        except Exception:
            self.rollback()
            raise

    def commit(self) -> None:
        self._conn.commit()
        self._set_search_path()

    def rollback(self) -> None:
        self._conn.rollback()
        try:
            self._set_search_path()
        except Exception:
            # _set_search_path() may fail if called inside a psycopg3
            # Transaction context or if the connection is in a bad state.
            # The rollback itself succeeded, so this is non-fatal.
            pass

    @contextmanager
    def transaction(self) -> Iterator["PgConnection"]:
        """Run a block in a psycopg transaction context.

        psycopg commits on normal exit and rolls back on exception exit.  The
        search_path is restored afterwards because rollback can discard session
        state established inside the transaction.
        """
        try:
            with self._conn.transaction():
                yield self
        finally:
            self._set_search_path()

    def close(self) -> None:
        self._conn.close()


def _translate_placeholders(query: str) -> str:
    return query.replace("?", "%s")


def _schema_from_path(db_path: str | Path | None) -> str:
    return os.environ.get("HERDMASTER_DB_SCHEMA", DEFAULT_SCHEMA_NAME)


def _normalize_schema_name(schema_name: str) -> str:
    schema_name = schema_name.strip()
    if not re.fullmatch(r"[A-Za-z_][A-Za-z0-9_]*", schema_name):
        raise ValueError(f"invalid Postgres schema name: {schema_name!r}")
    return schema_name


def _database_url(db_path: str | Path | None, database_url: str | None) -> str:
    if database_url:
        return database_url
    if db_path is not None and str(db_path).startswith(("postgresql://", "postgres://")):
        return str(db_path)
    return os.environ.get("DATABASE_URL") or DEFAULT_DATABASE_URL


def connect(
    db_path: str | Path | None = None,
    *,
    database_url: str | None = None,
    schema_name: str | None = None,
) -> PgConnection:
    """Open a Postgres connection for HerdMaster.

    ``db_path`` is retained for call-site compatibility. When it is not a
    Postgres URL, it is used only to derive an isolated schema name for tests.
    Runtime connections use ``DATABASE_URL`` or the local AOP Postgres default.
    """
    schema = _normalize_schema_name(schema_name or _schema_from_path(db_path))
    conn = psycopg.connect(_database_url(db_path, database_url), row_factory=dict_row)
    return PgConnection(conn, schema)


