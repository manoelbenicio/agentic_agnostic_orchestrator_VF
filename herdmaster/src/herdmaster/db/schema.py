def _pool_reset(conn: psycopg.Connection[Any]) -> None:
    """Reset callback invoked when a connection is returned to the pool.

    DECISION: Reset search_path and DISCARD TEMP to clear session state
    SOURCE:   https://www.psycopg.org/psycopg3/docs/advanced/pool.html
    REF:      "Connections life cycle" → reset callback
    RATIONALE: Ensures returned connections have clean state for next consumer.
    VERSION:  psycopg_pool 3.2.x
    """
    try:
        conn.execute("DISCARD TEMP")
        conn.execute("RESET ALL")
    except Exception:
        logger.warning("pool reset callback failed; connection will be discarded", exc_info=True)
        raise


def _pool_reconnect_failed(pool: Any) -> None:
    """Callback invoked when the pool exhausts reconnect_timeout.

    DECISION: Log critical error; do NOT call sys.exit() so orchestrator can decide
    SOURCE:   https://www.psycopg.org/psycopg3/docs/advanced/pool.html
    REF:      "Pool connection and sizing" → reconnect_failed callback
    RATIONALE: Alerting via logging; Kubernetes/systemd liveness probes handle restart.
    VERSION:  psycopg_pool 3.2.x
    """
    logger.critical(
        "DB connection pool reconnect FAILED after timeout — pool stats: %s",
        pool.get_stats() if hasattr(pool, "get_stats") else "unavailable",
    )


def connect_pool(
    conninfo: str | None = None,
    *,
    min_size: int = 2,
    max_size: int = 10,
    max_idle: float = 300.0,
    max_lifetime: float = 3600.0,
    reconnect_timeout: float = 300.0,
    timeout: float = 30.0,
    schema_name: str | None = None,
    open: bool = True,
    wait: bool = True,
    wait_timeout: float = 30.0,
    kwargs: dict[str, Any] | None = None,
) -> "ConnectionPool":
    """Create a psycopg ConnectionPool with robustness callbacks.

    DECISION: Use ConnectionPool.check_connection as the check callback
    SOURCE:   https://www.psycopg.org/psycopg3/docs/api/pool.html
    REF:      ConnectionPool constructor — check parameter
    RATIONALE: check_connection pings the server; broken connections are
              detected and replaced before being handed to the caller.
    VERSION:  psycopg_pool 3.2.x

    Returns the pool object.  The caller is responsible for closing it.
    """
    if ConnectionPool is None:
        raise ImportError(
            "psycopg_pool is required for connection pooling. "
            "Install with: pip install 'psycopg[pool]'"
        )

    conninfo = conninfo or os.environ.get("DATABASE_URL") or DEFAULT_DATABASE_URL
    extra_kwargs: dict[str, Any] = {"row_factory": dict_row}
    if kwargs:
        extra_kwargs.update(kwargs)

    schema = _normalize_schema_name(
        schema_name or os.environ.get("HERDMASTER_DB_SCHEMA", DEFAULT_SCHEMA_NAME)
    )

    def _configure(conn: psycopg.Connection[Any]) -> None:
        """Set search_path on every freshly-created connection."""
        with conn.cursor() as cur:
            cur.execute(
                sql.SQL("SET search_path TO {}, public").format(sql.Identifier(schema))
            )

    pool = ConnectionPool(
        conninfo=conninfo,
        min_size=min_size,
        max_size=max_size,
        max_idle=max_idle,
        max_lifetime=max_lifetime,
        reconnect_timeout=reconnect_timeout,
        timeout=timeout,
        open=open,
        check=ConnectionPool.check_connection,
        reset=_pool_reset,
        reconnect_failed=_pool_reconnect_failed,
        configure=_configure,
        kwargs=extra_kwargs,
    )

    if open and wait:
        pool.wait(timeout=wait_timeout)
        logger.info(
            "HerdMaster connection pool ready — stats: %s",
            pool.get_stats(),
        )

    return pool


def pool_get_stats(pool: Any) -> dict[str, Any]:
    """Return pool statistics suitable for Prometheus export.

    DECISION: Expose get_stats() counters for monitoring
    SOURCE:   https://www.psycopg.org/psycopg3/docs/api/pool.html
    REF:      ConnectionPool.get_stats()
    RATIONALE: Surfaces pool_size, pool_available, requests_waiting,
              connections_lost, returns_bad, etc. for alerting.
    VERSION:  psycopg_pool 3.2.x
    """
    if hasattr(pool, "get_stats"):
        return dict(pool.get_stats())
    return {}


def init_db(conn: PgConnection) -> None:
    """Create the HerdMaster Postgres schema and indexes idempotently."""
    with conn._conn.cursor() as cur:
        cur.execute(sql.SQL("CREATE SCHEMA IF NOT EXISTS {}").format(sql.Identifier(conn.schema_name)))
    conn.commit()
    # Split POSTGRES_SCHEMA_SQL by semicolons to execute each statement individually.
    # This avoids psycopg3 prepared statement errors with multi-statement queries.
    for statement in POSTGRES_SCHEMA_SQL.split(";"):
        stmt = statement.strip()
        if stmt:
            conn.execute(stmt)
    conn.commit()
    migrate_ottl_columns(conn)
    migrate_all_agent_foreign_keys(conn)
    cleanup_orphan_hm_schemas(conn)


def migrate_ottl_columns(conn: PgConnection) -> None:
    """Apply idempotent OTTL task lifecycle schema extensions."""
    statements = [
        "ALTER TABLE tasks ADD COLUMN IF NOT EXISTS estimate_minutes INTEGER",
        "ALTER TABLE tasks ADD COLUMN IF NOT EXISTS subtasks TEXT",
        "ALTER TABLE tasks ADD COLUMN IF NOT EXISTS progress TEXT",
        "ALTER TABLE tasks ADD COLUMN IF NOT EXISTS acceptance_criteria TEXT",
    ]
    with conn._conn.cursor() as cur:
        for statement in statements:
            cur.execute(statement)
    conn.commit()


def migrate_all_agent_foreign_keys(conn: PgConnection) -> list[str]:
    """Migrate agent foreign keys in every HerdMaster schema currently present."""
    schemas = [conn.schema_name]
    with conn._conn.cursor() as cur:
        cur.execute(
            """
            SELECT schema_name
            FROM information_schema.schemata
            WHERE schema_name LIKE 'hm\\_%%' ESCAPE '\\'
            ORDER BY schema_name
            """
        )
        for row in cur.fetchall():
            schema_name = str(row["schema_name"])
            if schema_name not in schemas:
                schemas.append(schema_name)

    migrated: list[str] = []
    for schema_name in schemas:
        if _schema_has_table(conn, schema_name, "agents"):
            migrate_agent_foreign_keys(conn, schema_name)
            migrated.append(schema_name)
    return migrated


def migrate_agent_foreign_keys(conn: PgConnection, schema_name: str | None = None) -> None:
    """Force all FKs that reference agents(id) to their expected delete rules."""
    target_schema = _normalize_schema_name(schema_name or conn.schema_name)
    with conn._conn.cursor() as cur:
        for (table_name, column_name), delete_rule in AGENT_FK_POLICIES.items():
            constraint_name = f"{table_name}_{column_name}_fkey"
            cur.execute(
                """
                SELECT tc.constraint_name
                FROM information_schema.table_constraints tc
                JOIN information_schema.key_column_usage kcu
                  ON tc.constraint_schema = kcu.constraint_schema
                 AND tc.constraint_name = kcu.constraint_name
                JOIN information_schema.constraint_column_usage ccu
                  ON ccu.constraint_schema = tc.constraint_schema
                 AND ccu.constraint_name = tc.constraint_name
                WHERE tc.constraint_type = 'FOREIGN KEY'
                  AND tc.table_schema = %s
                  AND tc.table_name = %s
                  AND kcu.column_name = %s
                  AND ccu.table_schema = %s
                  AND ccu.table_name = 'agents'
                  AND ccu.column_name = 'id'
                """,
                (target_schema, table_name, column_name, target_schema),
            )
            for row in cur.fetchall():
                cur.execute(
                    sql.SQL("ALTER TABLE {}.{} DROP CONSTRAINT {}").format(
                        sql.Identifier(target_schema),
                        sql.Identifier(table_name),
                        sql.Identifier(str(row["constraint_name"])),
                    )
                )
            cur.execute(
                sql.SQL(
                    "ALTER TABLE {}.{} ADD CONSTRAINT {} "
                    "FOREIGN KEY ({}) REFERENCES {}.agents(id) ON DELETE "
                ).format(
                    sql.Identifier(target_schema),
                    sql.Identifier(table_name),
                    sql.Identifier(constraint_name),
                    sql.Identifier(column_name),
                    sql.Identifier(target_schema),
                )
                + sql.SQL(delete_rule)
            )
    conn.commit()


def _schema_has_table(conn: PgConnection, schema_name: str, table_name: str) -> bool:
    with conn._conn.cursor() as cur:
        cur.execute(
            """
            SELECT 1
            FROM information_schema.tables
            WHERE table_schema = %s AND table_name = %s
            """,
            (schema_name, table_name),
        )
        return cur.fetchone() is not None


def cleanup_orphan_hm_schemas(conn: PgConnection, active_schema: str | None = None) -> list[str]:
    """Drop orphan HerdMaster hash schemas, preserving the active fixed schema."""
    keep = _normalize_schema_name(active_schema or conn.schema_name)
    dropped: list[str] = []
    with conn._conn.cursor() as cur:
        cur.execute(
            """
            SELECT schema_name
            FROM information_schema.schemata
            WHERE schema_name LIKE 'hm\\_%%' ESCAPE '\\'
              AND schema_name <> %s
            ORDER BY schema_name
            """,
            (keep,),
        )
        for row in cur.fetchall():
            schema_name = str(row["schema_name"])
            cur.execute(sql.SQL("DROP SCHEMA IF EXISTS {} CASCADE").format(sql.Identifier(schema_name)))
            dropped.append(schema_name)
    conn.commit()
    return dropped
